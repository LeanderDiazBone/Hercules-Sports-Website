      <h3>Wurfarten</h3>
      <p>Es sind zwei Wurfarten möglich, einmal im <b>Stehen</b> und einmal im <b>Sprung</b>. Bei beiden Formen bleibt die Technik gleich.</p>
      <h3>Wurftechnik</h3>
      <h4>1) Der Griff:</h4>
      <p>Der Wurf auf einen Basketballkorb beginnt bereits beim <b>Fangen des Balls</b>. Hier sollte der Ball schon so in den Händen liegen, wie er kurz darauf auch geworfen wird:</p>
      <p>Der <b>Wurfarm</b>, mit welchem der Ball geworfen wird, stützt den Ball von unten. Die andere Hand hält den Ball auf der Seite, damit er nicht zur Seite und damit vom Wurfarm fällt.</p>
      <h4>2) Die Fußstellung:</h4>
      <p>Beide Füße zeigen in Wurfrichtung und der Fuß auf der Seite des Wurfarms wird ein Stück nach vorne gesetzt. </p>
      <h4>3) Die Wurfbewegung:</h4>
      <p>Zuerst werden die Arme hochgenommen, sodass die <b>Ellbogen</b> auf der Höhe der Schulter sind. (Der Ball liegt auf den Fingern und nicht auf der Handfläche!), wenn der Ellbogen 90° zu der Schulter steht wird das <b>Handgelenk</b> nach vorne <b>geklappt</b> und der Ball nach oben und nicht nach vorne geworfen.</p>
      <h3>Passarten</h3>
      <p>Es sind zwei Passarten möglich, die im Basketball verwendet werden. Zum einen ein direkter <b>Druckpass</b>, beim dem der Ball ohne den Boden zu berühren zum Mitspieler geworfen wird und zum anderen der <b>Bodenpass</b> bei dem der Ball auf dem Weg zum Mitspieler einmal den Boden berührt um den gegnerischen Armen zu weichen.</p>
      <h3>Dribbeltechnik</h3>
      <p>Beim Dribbeln sind die Knie und der Oberkörper leicht gebeugt und der Ball wird ab der Hüfte geprellt. Die Finger sollten nicht zu angespannt aber auch nicht zu locker sein. Der Blick ist während des Dribblings nach <b>vorne</b> gerichtet, da so der Spieler die Situation gut überblicken kann.</p>



