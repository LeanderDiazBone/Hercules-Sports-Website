      <h3>Europameisterschaften</h3>
      <p>Die Tischtennis-Europameisterschaft (TT-EM) für Erwachsene wird seit 2008 jährlich durchgeführt. Eingeführt wurde die TT-EM 1958, Veranstalter ist die Europäische TT-Union ETTU. Es dürfen nur 16 Mannschaften teilnehmen.</p>
      <h3>Weltmeisterschaften</h3>
      <p>Die Tischtennisweltmeisterschaften (TT-WM) finden jährlich statt, und zwar in den Jahren mit ungerader Jahreszahl die <b>Einzel</b>-Weltmeisterschaften und in den geraden Jahren die <b>Mannschafts</b>-Weltmeisterschaften. Veranstalter ist der <b>Weltverband ITTF</b>. Bei jeder TT-WM tagt der ITTF-Kongress (Annual General Meeting) und berät über die TT-Regeln. Änderungen werden von diesem Gremium beschlossen. </p>
      <h3>World Cup</h3>
      <p>Der World Cup ist ein Tischtennisturnier, das vom Tischtennis-Weltverband ITTF veranstaltet wird. Mit der Organisation des Turniers beauftragt der ITTF ein Mitgliedsland. Es werden Einzelwettbewerbe für Damen und Herren – der World Cup – sowie Mannschaftswettbewerbe für Damen und Herren – der World Team Cup – durchgeführt.</p>
      <h3>World Tour Grand Finals</h3>
      <p>Das ITTF World Tour ist eine <b>Serie von Tischtennisturnieren</b>, die vom Weltverband ITTF jährlich durchgeführt wird. Es werden für Damen und Herren, Einzel-, Doppel- und U-21-Wettbewerbe ausgetragen. Dabei stehen Preisgelder zur Verfügung.</p>
      <h3>Europäisches Ranglistenturnier</h3>
      <p>Das Europäische Ranglistenturnier im Tischtennis wird von der Europäische TT-Union ETTU seit 1971 jährlich veranstaltet.</p>
