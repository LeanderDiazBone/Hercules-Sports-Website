    <h3>Gesundheit</h3>
    <p>Das Verletzungsrisiko im Bodybuilding ist an sich sehr gering. Jedoch ist <b>Dopen</b> ein weit verbreitetes Thema, mit dem nicht zu spaßen ist.</p>
    <p>Laut Expertenmeinungen können die Körperdefinitionen und Muskelmassen im Bodybuilding nicht ohne Doping Mittel erreicht werden, diese sind meistens <b>anabole Steroide</b> oder <b>Hormonpräparate</b>, sowie Diuretika (Entwässerungsmittel). Der Bodybuilder kauft diese auf dem Schwarzmarkt ein und konsumiert sie in verschiedensten Mischungen. Es kann aber auch sein, dass man solche Mittel auch über den Arzt verschieben bekommt. Dabei verursachen die Dopingmittel <b>starke Schäden an den inneren Organen</b>, welche lange Zeit nicht spürbar sind. Doch nach einer gewissen Zeit kann es zu plötzlichen Herzinfarkten kommen, so sind auch schon <b>zahlreiche Todesfälle</b> im Bereich Bodybuilding zustande gekommen. Im Profi-Bereich der Bodybuilder wird ein Dopinggebrauch von hundert Prozent geschätzt. Diese Konsumenten geben Monatlich ca. 5000 Euro für solche Mittel aus.  </p>
    <p>In der Gegenbewegung entstand so der <b>Natural Bodybuilding</b> (engl.  ,,natural  --- dt. natürlich) Bereich, in welchem die Bodybuilder auf solche Substanzen und Mittel verzichten und ihre Muskeln nur mit Training aufbauen.</p>
    <h3>Warum dopen so viele Bodybuilder?</h3>
    <p>Wenn man als naturaler Bodybuilder für einen Wettkampf trainiert, dann stößt man irgendwann an seine Grenzen; der Muskelwachstum bleibt trotz perfektem Training und Ernährung aus. Dies nennt man das „naturale Limit‘‘. Jeder Sportler hat sein eigens naturales Limit, welches hauptsächlich von der Genetik abhängt.</p>
    <p>Durch Doping kann diese Grenze überwunden werden und die Muskeln über ihren naturell größtmöglichen Zustand gebracht werden. Außerdem verleiten spezielle Doping-Stoffe zu einem schnelleren Muskelzuwachs.</p>
