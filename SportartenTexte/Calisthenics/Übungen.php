    <h3>5) Hängendes Beinheben</h3>
    <p>Die <b>untere Bauchmuskulatur</b> wird nur in sehr wenigen Übungen gefordert. Hängendes Beinheben gehört somit zu den wichtigsten Übungen was die untere Bauchmuskulatur betrifft. Eine hoch gelegene Stange ist bei dieser Übung sehr wichtig und unumgänglich, da der Körper komplett gestreckt sein sollte. Nun werden die gestreckten Beine so hoch wie möglich angehoben.</p>
    <p>Wenn dabei die Beine nicht wirklich in die Höhe kommen, kann die Übung vereinfacht werden, indem die Beine angewinkelt (Hockposition) werden.</p>
    <p>Trainierte Muskelgruppen, die im Fokus liegen:</p>
