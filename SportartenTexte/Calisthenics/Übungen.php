    <h3>1) Liegestütze</h3>
    <p>Eine Liegestütze kann fast jeder, doch sobald es um <b>mehrere Wiederholungen</b> oder die <b>korrekte Technik</b> geht, springen viele ab.</p>
    <p>Liegestützen sind eine ,,Allrounder-Übung‘‘; überall und ohne Ausrüstung sind sie zu machen und trainieren dabei den <b>gesamten Körper</b>. Je nachdem wie weit die Hände voneinander entfernt sind, desto stärker wird Trizeps oder Brust belastet; einarmig oder zweiarmig, oder mit Anheben eines Beines. Die große Varietät der Liegestütze macht sie zu einer erstklassigen Übung.</p>
    <p>Wichtig auch bei dieser relativ einfachen Übung ist die richtige Technik. Der gesamte Körper sollte von Fuß zu Kopf eine gerade Linie bilden. Um dies zu kontrollieren ist es sinnvoll eine zweite Person zuschauen zu lassen oder die Übung neben einem Spiegel zu machen.</p>
