    <h3>3) Klimmzüge</h3>
    <p>Die letzte der drei Grundübungen sind die Klimmzüge. Ein Klimmzug sollte von jedem Calisthenics-Athleten ausführbar sein, da er die <b>Basis vieler weiterer Übungen</b> ist. </p>
    <p>Trotz dessen ist ein Klimmzug keine leichte Übung und zu Beginn sehr fordernd. Um trotzdem Klimmzüge zu machen, kann ein elastisches Gummi-/Fitnessband(Link zu Shop) um die Klimmzugstange gehängt und die Füße in das Band gestellt werden. Somit gibt das Band von unten Unterstützung und der Klimmzug kann langsam geübt werden.</p>
    <p>Trainierte Muskelgruppen, die im Fokus liegen:</p>
    <ul>
      <li>Rücken</li>
      <li>Latissimus</li>
      <li>Bizeps</li>
    </ul>
    <h3>4) Plank</h3>
    <div class="Absatz65">
      <p>Die Plank ist eine klasse Übung um den gesamten <b>Rumpf zu stärken</b>. Dieser Teil des Körpers ist mit der wichtigste im Calisthenics. Auch für Einsteiger ist diese Übung gut geeignet, da sie nicht zu anspruchsvoll ist.</p>
      <p>Die besten Fortschritte können erzielt werden, wenn bei jedem Training die Planck ein paar Sekunden länger gehalten wird.</p>
    </div>
