				<h3>Was ist Calisthenics?</h3>
					<p>Calisthenics ist eine Form des körperlichen Trainings, bei der man nur Übungen mit dem eigenen Körpergewicht ausführt. Hier werden keine Trainingsgeräte oder Zubehör benötigt. Man führt diesen Sport aus ähnlichen Gründen wie beim Kraftsport aus, zum Beispiel dass man Muskeln aufbauen möchte um einerseits körperlich besser auszusehen, andererseits um mehr Kraft zu besitzen. Sportler, die diesen Sport dem Kraftsport vorziehen nennen meistens die Gründe, dass man es draußen machen kann und nicht ins Fitnessstudio gehen muss. Außerdem können Fortgeschrittene oft beeindruckende Übungen an einer Stange, während Kraftsportler oft nur Gewichte bewegen können.</p>
